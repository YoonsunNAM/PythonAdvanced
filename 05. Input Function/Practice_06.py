'''

    화씨 온도를 입력받아서 섭씨온도로
    변환하는 코드를 작성해보세요.

    섭씨온도 공식 : 5.0 / 9.0 * ( 화씨온도 - 32 )

    섭씨 온도를 입력받아서 화씨온도로
    변환하는 코드를 작성해보세요.

    화씨온도 공식 : 9.0 * 섭씨 / 5 + 32

'''


f_temp = float(input("# 화씨온도 : "))

c_temp = 5.0 / 9.0 * ( f_temp - 32 )

print("화씨온도 : %.1f  => 섭씨온도 : %.1f" % (f_temp , c_temp))

print("\n")

c_temp = float(input("# 섭씨온도 : "))

f_temp = 9.0 * c_temp / 5 + 32

print("섭씨온도 : %.1f  => 화씨온도 : %.1f" % (c_temp , f_temp))

