### 분기문 ###
'''

 - [형식]

    if 조건식:
        명령어

 - 조건식을 명시하고 , 조건식이 True(참)일경우 수행되는 구문

 +@ 파이썬에서는 switch ~ case구문이 없다.

 - if문안에 종속된 명령어의 범위는 들여쓰기로 구분됩니다.(문법)
 - 들여쓰기는 1칸이던지,4칸이던지 상관없이 들여쓰기 줄만 일치하면 됩니다.
 - if문 안의 들여쓰기는 관용적으로 tab을 사용합니다.


'''

# if문
print("---- start ----")
data = 60
if data >= 60:
    print("명령어1")
    print("명령어2")
    print("명령어3")

print("---- end ----")

