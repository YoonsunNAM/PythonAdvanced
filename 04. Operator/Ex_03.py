# 비교( 관계 ) 연산자

data1 = 100
data2 = 99

print( data1 > data2 )
print( data1 >= data2 )
print( data1 < data2 )
print( data1 <= data2 )

print( data1 == data2 )   # 같다.      = 와 혼동되면 안됩니다.
print( data1 != data2 )   # 같지않다.

#[+@] 다른언어는 불가능한데 파이썬 가능한 구문
print( data2 < 99.5 < data1 )





