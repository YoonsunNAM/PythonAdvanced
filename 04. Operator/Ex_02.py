# 할당 ( 복합대입 ) 연산자

data = 10
print(data)
print(data + 10)
print(data)

data += 30  # data = data + 30
print(data)

data -= 30  # data = data - 30
print(data)

data *= 30 # data = data * 30
print(data)

data /= 30 # data = data / 30
print(data)

data %= 30 # data = data % 30
print(data)

data // 30 # data = data // 30
print(data)
