### 연산자 ( Operator ) ###

'''
    - 컴퓨터에게 특정한 명령을 내리는 기호 , 부호

'''

# 산술 연산자
data1 = 100
data2 = 10

print(data1 + data2)     # 더하기
print(data1 - data2)     # 빼기
print(data1 * data2)     # 곱하기
print(data1 / data2)     # 나누기 (정수/정수 =>자동 실수로 형변환)
print(data1 % data2)     # 나눈 나머지값만
print(data1 // data2)    # 나눈 몫만
print()




